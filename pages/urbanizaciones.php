		</p>
      <p class="texto-parrafo">La noci&oacute;n de urbanizaci&oacute;n var&iacute;a seg&uacute;n el pa&iacute;s. Los espa&ntilde;oles utilizan el concepto para nombrar a las zonas residenciales que se encuentran en las afueras de los n&uacute;cleos urbanos. En Bolivia, en cambio, la urbanizaci&oacute;n est&aacute; vinculada al desarrollo urbano planificado, con viviendas que comparten una estructura similar.
		</p>
		<p class="texto-parrafo">Se conoce como proceso de urbanizaci&oacute;n al fen&oacute;meno de desarrollo de ciudades. Este proceso se lleva a cabo a partir de la migraci&oacute;n de las personas que residen en &aacute;reas rurales hacia la zona urbana  en b&uacute;squeda de mejor calidad de vida (gracias al desarrollo de los servicios sanitarios y educativos), posibilidades de empleo o una mayor oferta de ocio.
		</p>
      <p class="texto-parrafo">La tasa de urbanizaci&oacute;n, por otra parte, es un &iacute;ndice que refleja la relaci&oacute;n porcentual entre los habitantes de las ciudades (es decir, la poblaci&oacute;n urbana) y la cantidad total de habitantes de un pa&iacute;s. Una elevada tasa de urbanizaci&oacute;n indica un alto nivel de desarrollo. Los expertos afirman que reci&eacute;n en los &uacute;ltimos a&ntilde;os la tasa de urbanizaci&oacute;n mundial super&oacute; el 50%, lo que quiere decir que m&aacute;s de la mitad de la poblaci&oacute;n global vive en ciudades.</p>  
      
