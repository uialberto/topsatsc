        <h4 style="color: #666666; font-weight: bold; line-height: 1.25;">Urbanizaciones</h4>
        <p class="texto-parrafo">Urbanizaci&oacute;n es la acci&oacute;n y efecto de urbanizar y el n&uacute;cleo residencial urbanizado. El t&eacute;rmino suele utilizarse para nombrar al conjunto de construcciones levantadas en un antiguo medio rural. 
        </p>
        <p class="texto-parrafo">A la hora de desarrollar la urbanizaci&oacute;n de un terreno, &eacute;ste suele dividirse en diversas entidades (pol&iacute;gonos, manzanas, parcelas, etc.) a fin de construir las viviendas y la infraestructura necesaria. Una urbanizaci&oacute;n requiere de electricidad, agua potable, recolecci&oacute;n de residuos y transporte, entre otros servicios b&aacute;sicos para sus vecinos.</p>
