        <h4 style="color: #666666; font-weight: bold; line-height: 1.25;">Deslindes</h4>
        <p class="texto-parrafo"><strong>El deslinde catastral</strong> es un trabajo físico elaborado 
            por un cuerpo de t&eacute;cnicos top&oacute;grafos, los cuales se&ntilde;alan mediante marcas en un terreno verificando 
            de donde a donde pertenecen los limites perimetrales de la propiedad.  Se utiliza cuando existe 
            duda de los datos mostrados en la escritura o titulo de propiedad (Plano). Se entiende pues por deslinde 
            al acto formal de distinguir los l&iacute;mites de una propiedad. (el deslinde no indica qui&eacute;n es 
            el propietario de la propiedad). 
        </p>
        <p class="texto-parrafo">
            <div align="center" class="img-topografo">
                <img src="img/deslindestopo.jpg"  title="Plano Parcelamientos" alt="Plano Parcelamientos" width="400px" height="250px" />
            </div>
        </p>
        <h5 style="color: #666666; font-weight: bold; line-height: 1.25;">¿Cu&aacute;ndo podemos realizarlo?</h5>
        <p class="texto-parrafo">Es com&uacute;n realizarlo cuando hay un desacuerdo entre los propietarios de propiedades 
            vecinas por una divisoria poco clara. Es importante, una vez realizado el deslinde, la conformidad 
            y acuerdo entre las partes afectadas de la situaci&oacute;n f&iacute;sica del linde marcado. Si esto no fuera 
            posible habr&iacute;a que recurrir a los Tribunales de justicia. En este caso, el juez re&uacute;ne a las partes 
            del conflicto para resolver el deslinde, de tal manera que los due&ntilde;os afectados pueden ir acompa&ntilde;ados 
            de peritos top&oacute;grafos para corroborar la autenticidad del deslinde efectuado aportando todos los datos 
            necesarios para este fin.</p>
        <h5 style="color: #666666; font-weight: bold; line-height: 1.25;">¿Existen Otros Tipos de Deslindes?</h5>
        <p class="texto-parrafo">Existen otro tipo de deslindes: cuando hay diferencia de opiniones entre la 
            Administraci&oacute;n encargada de realizar el deslinde como bienes de dominio p&uacute;blico (vías pecuarias, 
            expropiaciones, cauces p&uacute;blicos, etc.,) y los propietarios afectados por el mismo. Es necesario aportar 
            un estudio topogr&aacute;fico en el caso de que se recurra administrativamente o en los Tribunales de Justicia.
            Entre los motivos para efectuar el deslinde de terrenos  son por adquisici&oacute;n de herencia y para evitar intrusiones.
