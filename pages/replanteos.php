        <h4 style="color: #666666; font-weight: bold; line-height: 1.25;">Replanteos</h4>
        <p class="texto-parrafo">Antes de iniciar la ejecuci&oacute;n de una obra es necesario trasladar al 
        			terreno lo que el proyectista ha dibujado en los planos. La materializaci&oacute;n de forma adecuada 
              e inequ&iacute;voca en el terreno de los puntos b&aacute;sicos que definen un proyecto constituye el replanteo. 
              Un replanteo tiene como finalidad la representaci&oacute;n de un proyecto dentro de un emplazamiento real, 
              es el proceso inverso a la toma de datos.
        </p>
        <p class="texto-parrafo">El replanteo topogr&aacute;fico se hace siempre por puntos, situando cada punto sobre el terreno de forma individual, independientemente del resto. Cuando lo que se pretenda trasladar del plano al terreno sea un elemento no puntual, la planta de un edicio a construir, por ejemplo, ser&aacute; necesario descomponerlo en un n&uacute;mero adecuado de puntos que lo definan geom&eacute;tricamente, replantear estos sobre el terreno de forma independiente y unirlos posteriormente en el orden adecuado.</p>
