            <h4 style="color: #666666; font-weight: bold; line-height: 1.25;">Altimetria</h4>
            <p class="texto-parrafo">La altimetr&iacute;a o hipsometr&iacute;a, como tambi&eacute;n se la denomina, 
                es la rama de la topograf&iacute;a que se ocupa de estudiar el conjunto de procedimientos 
                y de m&eacute;todos que existen para poder determinar y representar la altura o cota de cada 
                punto respecto de un plano de referencia. Por ejemplo, gracias a la altimetr&iacute;a es posible 
                representar el relieve del terreno, tal es el caso de planos de curvas de nivel, perfiles, 
                entre otros. 
            </p>
            <p class="texto-parrafo">En el espec&iacute;fico caso de la aviaci&oacute;n, la 
                altimetr&iacute;a juega un papel destacado, ya que se encarga de establecer la 
                altitud de vuelo de la aeronave, o sea, la distancia vertical del avi&oacute;n 
                respecto del nivel del mar.
            </p>             
            <p class="texto-parrafo">
              <div align="center" class="img-topografo">
                <img src="img/altimetrias.jpg"  title="Altimetria" alt="Altimetria" width="65%" height="40%" />
              </div>  
            </p>
            <p class="texto-parrafo">La determinaci&oacute;n y el conocimiento de este valor son de vital 
                importancia para la aeronavegaci&oacute;n y estrictamente para la circulaci&oacute;n de las 
                aeronaves en aquellas secciones verticales de cualquier aerov&iacute;a, porque la circulaci&oacute;n 
                      se distribuye y organiza en niveles de vuelos que se hallan vinculados a la altitud. 
            </p>          
            <p class="texto-parrafo">El instrumento tradicional que se ocupa de presentarle la altitud al 
              piloto de una aeronave es el alt&iacute;metro, que permite obtener una lectura de la altitud 
              partiendo de la medición de la presión estática que rodea a la aeronave.
            </p>            
            <p class="texto-parrafo">
                <div align="center" class="img-topografo">
                 <img src="img/perfilaltimetrico.png"  title="Perfil Altimetrico de una Carretera" alt="Perfil Altimetrico de una Carretera" width="65%" height="40%" />
                </div>
            </p>
            <p class="texto-parrafo">Si bien la mayor utilizaci&oacute;n del alt&iacute;metro se da en la 
                aeronavegaci&oacute;n, ya que el mismo constituye uno de los elementos m&aacute;s de 
                seguridad que se acopla al resto de los instrumentos de vuelo m&aacute;s importantes 
                del avi&oacute;n, el alt&iacute;metro tambi&eacute;n es frecuentemente usado en algunos deportes y 
                  actividades en las cuales se sufren importantes desniveles de alturas, tal es 
                  el caso del monta&ntilde;ismo, el trekking, el ciclismo, el esqu&iacute;, la escalada, el 
                  paracaidismo, entre los m&aacute;s practicados.
            </p>          
            <p class="texto-parrafo">
                              Entonces, a instancias de cualquiera de las pr&aacute;cticas 
               reci&eacute;n mencionadas, el alt&iacute;metro se emplea para conocer 
               los desniveles que se van superando.
            </p>
            <p class="texto-parrafo">
                              En algunos casos la tecnolog&iacute;a ha influido 
               radicalmente y as&iacute; es que por ejemplo algunos de los 
               m&aacute;s modernos veloc&iacute;metros para bicicletas integran un 
               alt&iacute;metro permitiendo generar perfiles de la jornada 
               con la ayuda de una computadora.
            </p>
